// Include the input/output stream library for basic I/O operations
#include <iostream>

// Use the standard namespace to avoid prefixing with std::
using namespace std;

// Main function - where program execution begins
// Returns an integer (int) to the operating system
int main() {
    // cout: console output object
    // <<: output operator to send text to console
    // "Hello, World!": the text to display
    // endl: adds a newline and flushes output buffer
    cout << "Hello, World!" << endl;
    
    // Return 0 to indicate successful program completion
    return 0;
}